package p0211;

public class Loop {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++) {
			System.out.println(i);
		}
		for(int i=10;i>0;i--) {
			System.out.println(i);
		}
		for(int i=2;i<=10;i+=2) {
			System.out.println(i);
		}
		for(int i=1;i<=10;i+=2) {
			System.out.println(i);
		}
		for(int i=3;i<=10;i*=3) {
			System.out.println(i);
		}
		for(int i=2;i<=10;i*=2) {
			System.out.println(i);
		}
	}
}
